package com.ahaag.solticechallenge.model;

/**
 * Model class for data related to user's phone number
 */
public class Phone {

    private String mWork;
    private String mHome;
    private String mMobile;

    public String getmWork() {
        return mWork;
    }

    public void setmWork(String mWork) {
        this.mWork = mWork;
    }

    public String getmHome() {
        return mHome;
    }

    public void setmHome(String mHome) {
        this.mHome = mHome;
    }

    public String getmMobile() {
        return mMobile;
    }

    public void setmMobile(String mMobile) {
        this.mMobile = mMobile;
    }
}
